class Main {
  public static void main(String[] args) {
    //The value of i is -47 while the value of j is 45.
    int i = -47;
    int j = 35;
    System.out.println("The value of i is " + i
    + " while the value of j is " + j + ".\n");
    
    //We are not false yet.
    boolean done = false;
    System.out.println("We are not " + done + " yet.\n");
    
    //x -> 0.012
    //y -> 27.374
    double x = 0.012;
    double y = 2.7374e1;
    System.out.println("x -> " + x + "\ny -> " + y);
  }
}